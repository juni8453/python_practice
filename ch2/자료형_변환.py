# int -> float 변환
my_int = 1
change_float = float(my_int)
print(change_float)
print(type(change_float))

# int -> String 변환 가능
change_string = str(my_int)
print(change_string)
print(type(change_string))

# String -> list 변환 가능
# 'coding' -> ['c', 'o', 'd', 'i', 'n', 'g']
my_str = 'coding'
change_list = list(my_str)
print(change_list)
print(type(change_list))